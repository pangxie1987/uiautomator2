'''
题目：打印出所有的"水仙花数"，所谓"水仙花数"是指一个三位数，其各位数字立方和等于该数本身。
例如：153是一个"水仙花数"，因为153=1的三次方＋5的三次方＋3的三次方。

程序分析：利用for循环控制100-999个数，每个数分解出个位，十位，百位。
'''
'''
将一个多位的int转换成list 
i = 123
print(list(map(int, str(i))))
'''

for i in range(100, 999):
    m = list(map(int, str(i)))
    a = m[0]
    b = m[1]
    c = m[2]
    if a**3+b**3+c**3==i:
        print(i)
